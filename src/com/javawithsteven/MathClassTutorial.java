package com.javawithsteven;

public class MathClassTutorial {
    public static void main (String[] args){
        System.out.println("Using Maths Class in Java:");

        int numb1 = 18, numb2 = 24;
        System.out.println(Math.max(numb1,numb2));
        System.out.println(Math.min(numb1,numb2));
        System.out.println(Math.sqrt(18) + " and " +Math.sqrt(24));
        System.out.println(Math.abs(18));
        System.out.println(Math.abs(-18));
        System.out.println(Math.random());

    }
}
